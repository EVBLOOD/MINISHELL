#include "../../mini_shell.h"

int	ft_pwd(void)
{
	char	pwd[PATH_MAX];

	getcwd(pwd, PATH_MAX);
	write(1, pwd, ft_strlen(pwd));
	write(1, "\n", 1);
	return (0);
}